<template>
  <div>
    <!-- 面包屑导航区  -->
    <el-breadcrumb separator-class="el-icon-arrow-right">
      <el-breadcrumb-item :to="{ path: '/home' }">首页</el-breadcrumb-item>
      <el-breadcrumb-item><a href="/">商品管理</a></el-breadcrumb-item>
      <el-breadcrumb-item>商品分类</el-breadcrumb-item>
    </el-breadcrumb>

    <!-- 卡片视图区域 -->
    <el-card>
      <!-- 添加区域 -->
      <el-row :gutter="20">
        <!-- 添加分类 -->
        <el-col>
          <el-button type="primary" @click="showAddCateDialog">添加分类</el-button>
        </el-col>
      </el-row>

      <!-- 列表区 -->
      <tree-table class="el-table" :data="catelist" :columns="columns" :selection-type="false"
      :expand-type="false" show-index index-text="#" border
      :show-row-hover="false">
        <!-- 是否有效 -->
        <template slot="isok" slot-scope="scope">
          <i class="el-icon-success" v-if="scope.row.cat_deleted === false" style="color: lightgreen;"></i>
          <i class="el-icon-error" v-else style="color: red;"></i>
        </template>
        <!-- 排序 -->
        <template slot="order" slot-scope="scope">
          <el-tag size="mini" v-if="scope.row.cat_level === 0">一级</el-tag>
          <el-tag size="mini" type="success" v-else-if="scope.row.cat_level === 1">二级</el-tag>
          <el-tag size="mini" type="warning" v-else>三级</el-tag>
        </template>
        <!-- 操作 -->
        <template slot="opt" slot-scope="scope">
          <el-button type="primary" icon="el-icon-edit" size="mini"  @click="showEditDialog(scope.row.cat_id)">编辑</el-button>
          <el-button type="danger" icon="el-icon-delete" size="mini" @click="removeCateById(scope.row.cat_id)">删除</el-button>
        </template>
      </tree-table>

      <!-- 分页区域 -->
      <el-pagination
      @size-change="handleSizeChange"
      @current-change="handleCurrentChange"
      :current-page="queryInfo.pagenum"
      :page-sizes="[3, 5, 10,15]"
      :page-size="queryInfo.pagesize"
      layout="total, sizes, prev, pager, next, jumper"
      :total="total">
      </el-pagination>
    </el-card>

      <!-- 添加分类对话框 -->
      <el-dialog title="添加分类" :visible.sync="addCateDialogVisible"
      width="50%" @close="addCateDialogClosed">
          <!-- 内容主体区 -->
          <el-form :model="addCateForm" :rules="addCateFormRules" ref="addCateFormRef" label-width="100px">
              <el-form-item label="分类名称：" prop="cat_name">
                <el-input v-model="addCateForm.cat_name"></el-input>
              </el-form-item>
              <el-form-item label="父级分类：">
                <el-cascader :options="parentCateList"
                :props="cascaderProps"
                v-model="selectedKeys"
                @change="parentCateChanged"
                clearable props.checkStrictly>
                </el-cascader>
              </el-form-item>
          </el-form>
          <!-- 底部区域 -->
          <span slot="footer" class="dialog-footer">
            <el-button @click="addCateDialogVisible = false">取 消</el-button>
            <el-button type="primary" @click="addCate">确 定</el-button>
          </span>
      </el-dialog>

      <!-- 修改分类对话框 -->
      <el-dialog title="编辑分类" :visible.sync="editDialogVisible"
      width="50%" @close="editDialogClosed">
        <!-- 内容主体区 -->
        <el-form :model="editForm" :rules="addCateFormRules" ref="editFormRef" label-width="80px">
          <el-form-item label="分类名称" >
            <el-input v-model="editForm.cat_name"></el-input>
          </el-form-item>
        </el-form>
        <!-- 底部区域 -->
        <span slot="footer" class="dialog-footer">
          <el-button @click="editDialogVisible = false">取 消</el-button>
          <el-button type="primary" @click="editCateInfo">确 定</el-button>
        </span>
      </el-dialog>
  </div>
</template>

<script>
export default {
  name: '',
  data(){
    return {
      // 查询条件
      queryInfo:{
        type:3,
        pagenum:1,
        pagesize:5
      },
      // 商品分类的数据列表，默认为空
      catelist:[],
      // 总数据条数
      total:0,
      // 为table指定列的定义
      columns:[
        {
          label:'分类名称',
          prop:'cat_name'
        },
        {
          label:'是否有效',
          // 表示将当前列定义为模板列
          type:'template',
          // 表示当前这一列使用的模板名称
          template:'isok'
        },
        {
          label:'排序',
          // 表示将当前列定义为模板列
          type:'template',
          // 表示当前这一列使用的模板名称
          template:'order'
        },
        {
          label:'操作',
          // 表示将当前列定义为模板列
          type:'template',
          // 表示当前这一列使用的模板名称
          template:'opt'
        }
      ],
      // 控制添加分类对话框的显示与隐藏
      addCateDialogVisible:false,
      // 添加分类的表单数据
      addCateForm:{
        // 将要添加分类的名称
        cat_name:'',
        // 父级分类的id
        cat_pid:0,
        // 分类的等级，默认要添加的是一级分类
        cat_level:0
      },
      // 添加分类表单的验证规则对象
      addCateFormRules:{
          cat_name:[
            {
              required:true,
              message:'请输分类名',
              trigger:'blur'
            }
          ]
      },
      // 父级分类列表
      parentCateList:[],
      // 指定级联选择器配置对象
      cascaderProps:{
        // checkStrictly:true,
        expandTrigger:'hover',
        value:'cat_id',
        label:'cat_name',
        children:'children'
      },
      // 选中的父级分类的id数组
      selectedKeys:[],
      // 控制修改分类对话框的显示与隐藏
      editDialogVisible:false,
      // 查询到的分类信息对象
      editForm:{},
    }
  },
  components: {},
  created(){
    this.getCateList()
  },
  mounted(){},
  methods: {
    // 获取商品分类数据
    getCateList(){
      this.$axios({
        methods:'get',
        url:'categories',
        params:this.queryInfo
      }).then(res=>{
        // console.log(res.data)
        if(res.data.meta.status !== 200)
          return this.$message.error('获取商品分类失败！');
          // 把数据列表赋值给catelist
        this.catelist = res.data.data.result;
        // 为总数据条数赋值
        this.total = res.data.data.total;

      }).catch(function(err){
        console.log(err);
      })
    },
    // 监听pagesize改变
    handleSizeChange(newSize){
      this.queryInfo.pagesize=newSize;
      this.getCateList();
    },
    // 监听页码值改变的事件
    handleCurrentChange(newPage){
      this.queryInfo.pagenum=newPage;
      this.getCateList();
    },
    // 点击按钮展示添加分类对话框
    showAddCateDialog(){
      // 获取父级分类的数据列表
      this.getParentCateList();
      // 再展示出对话框
      this.addCateDialogVisible = true;
      // console.log('this.parentCateList')
    },
    // 获取父级分类的数据列表
    getParentCateList(){
      this.$axios.get('categories',{
        params:{type:2}
      }).then(res=>{
        if(res.data.meta.status !== 200){
          return this.$message.error('获取父级分类数据失败！');
        }
        this.$message.success('获取父级分类数据成功！');
        this.parentCateList = res.data.data;
        // console.log(this.cascaderProps)
      }).catch(err=>err)
    },
    // 选择项发生变化触发这个函数
    parentCateChanged(){
      // 如果selectedKeys数组中的length大于0，证明选中父级分类
      // 反之就说明没有选择任何父级分类
      if(this.selectedKeys.length>0){
        // 父级分类的id
        this.addCateForm.cat_pid=this.selectedKeys[this.selectedKeys.length-1];
        // 为当前分类等级赋值
        this.addCateForm.cat_level = this.selectedKeys.length;
        return;
      }else{
        // 父级分类的id
        this.addCateForm.cat_pid= 0;
        // 为当前分类等级赋值
        this.addCateForm.cat_level = 0;
      }
    },
    // 点击按钮添加新的分类
    addCate(){
      this.$refs.addCateFormRef.validate(valid=>{
        if(!valid) return
        this.$axios.post('categories',this.addCateForm).then(res=>{
          if(res.data.meta.status !== 201){
            return this.$message.error('添加分类失败！');
          }
          this.$message.success('添加分类成功！')
          this.getCateList();
          this.addCateDialogVisible = false;
        }).catch(err=>err)
      })
    },
    // 监听对话框的关闭事件，重置表单数据
    addCateDialogClosed(){
      this.$refs.addCateFormRef.resetFields();
      this.selectedKeys=[];
      this.addCateForm.cat_level = 0;
      this.addCateForm.cat_pid = 0;
    },
    // 展示编辑分类的对话框
    showEditDialog(id){
      // console.log(id)
      this.$axios.get('categories/' + id).then(res=>{
        if(res.data.meta.status !== 200){
          return this.$message.error(res.data.meta.msg);
        }
        this.editForm = res.data.data;
        this.editDialogVisible=true;
      }).catch(err=>{
        console.log(err);
      })
    },
    // 监听修改分类对话框的关闭事件
    editDialogClosed(){
      this.$refs.editFormRef.resetFields();
    },
    // 修改分类信息并提交
    editCateInfo(){
      this.$refs.editFormRef.validate(valid=>{
        if(!valid) return;
        // 发起分类信息请求
        this.$axios.put('categories/'+this.editForm.cat_id,{
          cat_name:this.editForm.cat_name
        }).then(res=>{
          if(res.data.meta.status !== 200){
            return this.$message.error('修改分类信息失败！');
          }
          // 关闭对话框
          this.editDialogVisible = false;
          // 刷新数据列表
          this.getCateList();
          // 提示修改成功
          this.$message.success('修改分类信息成功！')
        })
      })
    },
    // 根据id删除对应的角色信息
    async removeCateById(id){
       // 弹框询问用户是否删除数据
      const confirmResult = await this.$confirm('此操作将永久删除该商品分类, 是否继续?', '提示', {
        confirmButtonText: '确定',
        cancelButtonText: '取消',
        type: 'warning'
      }).catch(err=>err)
      // 如果用户确认删除，则返回值为字符串confirm
      // 如果用户取消了删除，则返回值为字符串cancel
      if(confirmResult != 'confirm'){
        return this.$message.info('已取消删除');
      }
      this.$axios.delete('categories/'+id).then(res=>{
        if(res.data.meta.status !== 200){
          return this.$message.error('删除商品分类失败！');
        }
        this.$message.success('删除商品分类成功！');
        this.getCateList();
      }).catch(err=>err)
    }
  }
}
</script>
<style lang="less" scoped>
  .el-cascader{
  width: 100%;
}
</style>
